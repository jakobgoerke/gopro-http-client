export const CounterSchema = z.object({
  length: z.number(),
  start: z.number(),
});

export const PresetSettingSchema = z.object({
  id: z.number(),
  isCaption: z.boolean(),
  value: z.number(),
});

export const PresetSchema = z.object({
  icon: zObjectKeys(PresetIconEnum),
  id: z.number(),
  isFixed: z.boolean().optional(),
  isModified: z.boolean(),
  titleNumber: z.number().optional(),
  userDefined: z.boolean(),
  mode: zObjectKeys(FlatModeEnum),
  titleId: zObjectKeys(PresetTitleEnum),
  settingArray: z.array(PresetSettingSchema).optional(),
});
export type Preset = z.infer<typeof PresetSchema>;

export const PresetGroupSchema = z.object({
  canAddPreset: z.boolean(),
  icon: zObjectKeys(PresetGroupIconEnum),
  id: zObjectKeys(PresetGroupEnum),
  modeArray: z.array(zObjectKeys(FlatModeEnum)).optional(),
  presetArray: z.array(PresetSchema),
});
export type PresetGroup = z.infer<typeof PresetGroupSchema>;

export const AvailablePresetsSchema = z.object({
  customIconIds: z.array(CounterSchema),
  customTitleIds: z.array(CounterSchema),
  presetGroupArray: z.array(PresetGroupSchema),
});
export type AvailablePresets = z.infer<typeof AvailablePresetsSchema>;
